/**
  * @brief  获取测量时间
  * @param  无
  * @retval 无
  * @attention  在 SysTick 中断函数 SysTick_Handler()调用
  */
void TmDelay_measure(void)
{
	if(measureflag)
	{
		TmMeasDelay ++;
		
		if(TmMeasDelay >= 11764)
		{
			TmMeasDelay = 0;
			measureflag = 0;
			TmMeasDelay = 0;
		}
	}
}

u32 Get_TmDelay_measure(void)
{
	return TmMeasDelay;
}

void TmDelay_measureSet(void)
{
	TmMeasDelay = 0;
	measureflag = 1;
}

void TmDelay_measureClr(void)
{
	TmMeasDelay = 0;
	measureflag = 0;
}


